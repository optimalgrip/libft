#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*new_str;
	size_t	i;
	size_t	c;

	c = ft_strlen(str) + 1;
	i = 0;
	new_str = (char *)malloc (c * sizeof(char));
	if (new_str == NULL)
		return (NULL);
	while (str[i])
	{
		new_str[i] = str[i];
		i++;
	}
	new_str[i] = '\0';
	return (new_str);
}
